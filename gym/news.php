                <h2>Katie Wagg awarded Princess Diana Award</h2>
                <p>
                	<img src="images/katie.jpg" alt="Katie" style="float: right; margin-top:0px; margin-left: 5px; border: none;" />
                	Katie Wagg, aged 14, from Derby, received her Diana Award on 16th May at Canary Wharf in London. Singer Sinitta presented Katie with the award, she received her award for her supporting after school activities at Merrill College and contributing to an after school boxing club at the Splash Fit facility, which is based in a deprived area. Katie gives up her time 3 days a week to help coach at the boxing club and she encourages young people to get involved in boxing and other physical activities.  During the school holidays she helps with the activities camps so that children can have the opportunity to come and try new sports. <br />
<br />
Sam Godfrey, owner of Splash Fit , nominated Katie for the Diana Award, he said: "Katie also trains and has just had her first competitive bout in boxing and as a result, she has become an even bigger role model and many other young women want to follow in her footsteps.  Katie is a fantastic leader, a good communicator and shows a real willingness to learn and teach others. Receiving the Diana Award is a fantastic achievement and a credit to the effort and hard work Katie puts into Splash Fit. The award will provide a stepping stone towards bigger things."
